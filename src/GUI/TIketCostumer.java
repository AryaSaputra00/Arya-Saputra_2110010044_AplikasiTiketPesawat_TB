    public void tampilDataTiketCostumer(){
        try {
            modelTabelTiketCostumer.getDataVector().removeAllElements();
            modelTabelTiketCostumer.fireTableDataChanged();
        
            PreparedStatement query = myObject.Konek.prepareStatement("SELECT * FROM tb_tiketcostumer");
            ResultSet data = query.executeQuery();
            while (data.next()) {
            Object[] baris = new Object[12];
            baris[0] = data.getString("nik");
            baris[1] = data.getString("nama");
            baris[2] = data.getString("jenis_kelamin");
            baris[3] = data.getString("alamat");
            baris[4] = data.getString("no_telpon");
            baris[5] = data.getString("kelas");
            baris[6] = data.getString("maskapai");
            baris[7] = data.getString("keberangkatan");
            baris[8] = data.getString("tujuan");
            baris[9] = data.getString("tanggal");
            baris[10] = data.getString("jam");
            baris[11] = data.getString("biaya");
            modelTabelTiketCostumer.addRow(baris);
            
            }
            query.close();
            data.close();
            } catch (Exception e){
            }
        
    
    }
    

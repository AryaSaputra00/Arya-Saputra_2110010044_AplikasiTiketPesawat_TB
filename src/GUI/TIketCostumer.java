    private void buttonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonHapusActionPerformed
        // TODO add your handling code here:
         // untuk menghapus data
        try {
    if (tfNIK.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "NIK belum di isi");
        tfNIK.requestFocus();
    } else {
        String sql = "DELETE FROM tb_tiketcostumer WHERE nik=?";
        try (PreparedStatement preparedStatement = myObject.Konek.prepareStatement(sql)) {
            preparedStatement.setString(1, tfNIK.getText());

            int rowsDeleted = preparedStatement.executeUpdate();

            if (rowsDeleted > 0) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus");
                tampilDataTiketCostumer();
            } else {
                JOptionPane.showMessageDialog(this, "Tidak ada data dengan NIK tersebut");
            }
        }
    }
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, e.toString());
}
    }//GEN-LAST:event_buttonHapusActionPerformed


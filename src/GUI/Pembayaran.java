    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanActionPerformed
        //button simpan buat menyimpan data
        try {
            
            if (tfIdpembayaran.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "ID belum di isi");
                tfIdpembayaran.requestFocus();
         
            }else{
                String sql = "insert into tb_pembayaran value ('"+tfIdpembayaran.getText()+"','"+tfNama.getText()+"','"+tfJenispembayaran.getText()+"','"+tfAlamat.getText()+"','"+tfEmail.getText()+"','"+tfJumlahpembayaran.getText()+"')";
                myObject.Konek.createStatement().execute(sql);
                tampilDataPembayaran();
                JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }
       
    }//GEN-LAST:event_buttonSimpanActionPerformed

    private void buttonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonHapusActionPerformed
        // untuk menghapus data
        try {
    if (tfIdpembayaran.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "ID belum di isi");
        tfIdpembayaran.requestFocus();
    } else {
        String sql = "DELETE FROM tb_pembayaran WHERE id_pembayaran=?";
        try (PreparedStatement preparedStatement = myObject.Konek.prepareStatement(sql)) {
            preparedStatement.setString(1, tfIdpembayaran.getText());

            int rowsDeleted = preparedStatement.executeUpdate();

            if (rowsDeleted > 0) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus");
                tampilDataPembayaran();
            } else {
                JOptionPane.showMessageDialog(this, "Tidak ada data dengan ID tersebut");
            }
        }
    }
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, e.toString());
}
    }//GEN-LAST:event_buttonHapusActionPerformed


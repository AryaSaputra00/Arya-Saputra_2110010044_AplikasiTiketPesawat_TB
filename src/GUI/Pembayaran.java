    public void tampilDataPembayaran(){
        try {
            modelTabelPembayaran.getDataVector().removeAllElements();
            modelTabelPembayaran.fireTableDataChanged();
        
            PreparedStatement query = myObject.Konek.prepareStatement("SELECT * FROM tb_pembayaran");
            ResultSet data = query.executeQuery();
            while (data.next()) {
            Object[] baris = new Object[6];
            baris[0] = data.getString("id_pembayaran");
            baris[1] = data.getString("nama");
            baris[2] = data.getString("jenis_pembayaran");
            baris[3] = data.getString("alamat");
            baris[4] = data.getString("email");
            baris[5] = data.getString("jumlah_pembayaran");
            modelTabelPembayaran.addRow(baris);
            
            }
            query.close();
            data.close();
            } catch (Exception e){
            }
    
    }


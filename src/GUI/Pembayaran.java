    private void buttonUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUbahActionPerformed
        // untuk mengubah data
        try {
    if (tfIdpembayaran.getText().isEmpty()){
        JOptionPane.showMessageDialog(this, "ID belum di isi");
        tfIdpembayaran.requestFocus();
    } else {
        String idPembayaran = tfIdpembayaran.getText();
        String sql = "UPDATE tb_pembayaran SET nama = ?, jenis_pembayaran = ?, alamat = ?, email = ?, jumlah_pembayaran = ? WHERE id_pembayaran = ?";
        
        PreparedStatement preparedStatement = myObject.Konek.prepareStatement(sql);
        preparedStatement.setString(1, tfNama.getText());
        preparedStatement.setString(2, tfJenispembayaran.getText());
        preparedStatement.setString(3, tfAlamat.getText());
        preparedStatement.setString(4, tfEmail.getText());
        preparedStatement.setString(5, tfJumlahpembayaran.getText());
        preparedStatement.setString(6, idPembayaran);
        
        int rowsUpdated = preparedStatement.executeUpdate();
        
        if (rowsUpdated > 0) {
            tampilDataPembayaran();
            JOptionPane.showMessageDialog(this, "Data dengan ID " + idPembayaran + " berhasil diubah");
        } else {
            JOptionPane.showMessageDialog(this, "Tidak ada data dengan ID " + idPembayaran);
        }
    }
} catch (SQLException e) {
    JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
}

    }//GEN-LAST:event_buttonUbahActionPerformed

